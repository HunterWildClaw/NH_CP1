# NH 2nd Basic Calculator

num = float(input("Yo what's up my homie! Give me some random number off the top of your head. "))
num_1 = float(input("And I need another one to make this trick work. "))

print(f"{num} + {num_1} = {num + num_1}")
print(f"{num} - {num_1} = {num - num_1}")
print(f"{num} * {num_1} = {num * num_1}")
print(f"{num} / {num_1} = {num / num_1}")
print(f"{num} ** {num_1} = {num ** num_1}")
print(f"{num} % {num_1} = {num % num_1}")